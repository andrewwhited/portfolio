      <h3>Vision</h3>
      <p>I started by checking out as many books on vision and light reception as I could. I found books on everything from the physics of light, to Ophthalmology, and even a really old set of <a href="https://en.wikipedia.org/wiki/Ishihara_Test">Ishihara plates.</a> I started to learn about the types of color deficiencies and how the anatomy of the eye affect our perception of light. </p>        
      <h3>Logo</h3>
      <p>I explored quite a few options, but the direction that I thought worked best was one that hinted at some of the medical illustrations that I had seen. I wanted to find a way to actually represent the idea of color blindness in a simple way. In the logo I depicted light entering the eye as color, but hitting the retina as grey. </p>

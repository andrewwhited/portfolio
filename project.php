      <p>One of the major problems I had when using MARTA was that the type was very illegible from distances and angles, so I wanted to address this first in my redesign. Marta’s current typeface does not meet the standards laid out in the ADA. I tested different typefaces legibility by hanging printouts at the end of our hall and getting classmates to try to read them. I found that Gotham preforms well from an accessibility standpoint and appropriately communicates the proposed MARTA brand.</p>

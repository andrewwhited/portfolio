      <p>When iOS 7 went into beta, I closely followed the evolution of the design as it moved through releases. I endured with the frequent crashes, glitches, and problems associated with a piece of software that is in beta. The fact that my phone crashed every five minutes somehow seemed worth it because I felt like I was in the passenger seat of the product team as they worked out their design details. </p>
      <p>A part of the update that I was especially interested in was the call screen. It was just a few icons, a plain red bar that you could slide to end a call, and some text. Thats it. No fuss. Simple.</p>
      <p>But then something changed between the beta 4 and the beta 5 releases. They redid the call screen. The icons now had circle outlines and the ‘end call’ bar was changed.</p>
      <p> I (quite irrationally) disliked this change. I found out that the reason for the changes was due to user testing. People didn’t know they could click on the icon.  I found it hard to believe that if someone wanted to mute the call, they would not think to click on the thing that says mute. This was not a satisfying answer. I remember thinking (and probably ranting to my friends) about how Apple missed an opportunity to push their customers. People (millions of people at that) were going to use this interface. Even if the first time they tried to use it it took them an extra second or two to know to click on one of these buttons, they would get it by the second time. It would become normal for them by the third.</p>
      <p>I thought that this was an opportunity to undo some of our dependency on oversaturated visual clues; to educate the user away from the double bevels and glossy, lickable buttons of the past, and towards the subtle and refined flat icon instead. </p>
      <p>Now, that is way overdramatic. I have come to like the call screen and have seen how it has successfully evolved over iterations. Some of that change in visual literacy is happening to an extent as well. Whether I was off-base or not, I think my response brought up some important questions for me. Namely, what role does user testing play in the design practice, and what is our responsibility to improve our user’s visual literacy?</p>

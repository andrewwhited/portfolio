      <h3>Solving for variables</h3>
      <p>The design research process is about filling in this context so we can give our designs the best chance of resonating and communicating. Some context is static. The way that our human eyes react to and perceive light is a set part of our biology. The ingrained patterns and mathematics of our universe set part of the scene as well. Our basic design principles are all reactions to the constant aspects of  the world that we live in. That does’t paint a nearly detailed enough picture of the environments that we design for. We use our user research and user testing to fill in the gaps.</p>

      <h3>Teachers of what?</h3>
      <p>There are certain decisions that we should not delegate to our users. Through experience, practice, and education, we are able to go beyond the user’s expectations or imagination. The fact that we as designer’s have ‘expertise’ implies that there will be times that we will decide things that the user would not have chosen. This is ok. When we expose users to designs over time, we change their expectations. Designers shape the users’ visual literacy. We have a responsibility to progress it.</p>

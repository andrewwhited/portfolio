      <h3>Where are we going?</h3>
      <p>The place that this development is the most clear is in type design. The way that we have chosen to draw letters has changed drastically over time. While we struggle to read type set in blackletter, our medieval friends would have a similar problem with text set in Helvetica or Georgia. It is not just time that impacts our visual literacy either. Take for example the lettering of different subcultures. The way that we draw letters has been developed for centuries by countless people, cultures, and generations. They have all left this practice in very different, unique places. The way that we receive communication and assign value is unique to our context. This context is made up of many factors (time and location being some, but only some of many). It is because of this that I believe there is no ‘absolute beauty’ when it comes to design, no golden standard that a design can be held against devoid of its context. I think that there is truth in Rand’s words about aesthetic development, but I don’t think that the direction in which we are developing resides in the visual or concrete.</p>
      <p>This means that the goal is not a stationary target. Just like a mathematical function gives us a different value based on what we input, the ideal design decision is highly dependent on the variables surrounding it. I call this context. When we talk about design, we are not working with simple solve for x equations; we deal with complex landscapes made up of many unique users. Time is one variable, location is a variable too, so is the age of our user, and the industry we are in, and the previous experiences our users have encountered, and so on, and so on. These equations ask us to solve for the whole alphabet. </p>

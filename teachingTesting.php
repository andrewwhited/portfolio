      <h3>Setting Expectations</h3>
      <p>But what about teaching? If there is no single archetype of visual literacy, what then do we try to develop our users’ aesthetics into? What we teach is not to love helvetica or hate drop shadowed comic sans. Or even to prefer gracious white space. We need to step back from ‘what they like’  and ask ’why they like it.’ We should be teaching users to appreciate bespoke and contextual design, because there are times that Helvetica is not the right choice. By learning why helvetica works in some scenarios, we are able to know why it won’t work in others. The way that we train users to care for and seek contextual designs is to keep serving them designs that cater to their unique needs and presumptions. In some ways we need to teach our users to be spoiled. And if we are lucky, we will get users who hold us accountable and end up teaching us.</p>

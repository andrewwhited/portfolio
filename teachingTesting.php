      <h3>User is always right…</h3>
      <p>I currently work at IBM. User research and user testing is a huge part our new design initiative. For decades, we have built products that are not informed by our users and we have seen the miserable place this has left us. We take ‘user first’ very seriously; knowing the user is the only way we can make products that solve their problems. I am definitely not the user of the products that I design. I rely on research to find out if showing REST resources a certain way maps to their current understanding, how often are they going to need to add a new on-premise network, or how many Salesforce objects are they expecting to sync. These are questions that have direct consequences for the designs I work on. They are also questions that I cannot use my ‘design instinct’ on. In a lot of ways, I am dependent on using user research to design a quality product. I didn’t learn how to design an API creation app in design school. I learned how to listen to user’s needs, not what those needs are every time. I do not want to underemphasize my acknowledgment for the benefits of user testing.</p>

      <h3>…except when they aren’t</h3>
      <p>But, what I don’t want to see is a world where the role of designer becomes a middleman. It is really easy to take the idea of user first and extrapolate it to things like A/B testing every little design element. The role of a designer has been changing. In the golden ages of design, it was designer as Craftsmen. The designer’s best tool was his hand. This shifted when computers brought machined precision. Now that algorithms can generate infinite variations, the role of a designer could be seen as Curator. His primary tools are his eye and mind. Its not a stretch to imagine this role being instead delegated to the user. What color blue does the user want? The designer is then restricted to solely researcher, facilitating the testing and serving up what the user says they want. But, what about designer as expert. That’s a title I wouldn’t mind. Because, let’s be honest, users are often wrong. I admit I was wrong when I first judged the iOS 7 screens. Think about how often people complained whenever Facebook changed their layout. Do we really wish we still had the 2007 version? Testing is a very important tool in a designer’s repertoire, but it is one of many. </p>
